#
# Format: ``ck_<32+ alphanumeric chars>``. Issued via wp-admin >
# WooCommerce > Settings > Advanced > REST API > Add Key. Body
# generated by ``wp_generate_password(32, false)`` for modern WC; 40
# chars for intermediate versions; 64-char lowercase hex for legacy.


def test_secret_scanner_detects_woocommerce_consumer_key(tmp_path: Path) -> None:
    """WooCommerce Consumer Key: ``ck_<32 alphanumeric>``.

    Pre-fix every ``ck_`` token with a uniform character class body
    was SILENTLY UNDETECTED — the entropy fallback's
    ``_looks_like_secret`` heuristic requires multiple character
    categories, and a body like ``ck_aaaaaa...`` failed that check
    entirely. Mixed-case bodies were caught only by the generic
    "Verdächtige Zuweisung" assignment heuristic (depends on the
    variable name carrying a sensitive keyword), losing the
    WooCommerce-specific attribution that incident-response keys off
    (revocation at wp-admin > WooCommerce > Settings > Advanced >
    REST API).
    """
    file_path = tmp_path / "woocommerce_config.py"
    body = "a" * 32  # 32-char uniform body (silently undetected pre-fix)
    secret = f"ck_{body}"
    assert len(body) == 32
    file_path.write_text(
        f'WC_CONSUMER_KEY = "{secret}"', encoding="utf-8"
    )

    findings = scan_repository(tmp_path, paths=[file_path])

    assert findings, "Should detect WooCommerce Consumer Key"
    reasons = [f.reason for f in findings]
    assert "WooCommerce Consumer Key gefunden" in reasons, (
        f"Expected WooCommerce-specific attribution, got reasons: "
        f"{reasons}. WooCommerce Consumer Keys (paired with the matching "
        "cs_ secret) grant the issuing API user's full WooCommerce REST "
        "scope (product / order / customer / coupon / refund / shipping "
        "/ tax / webhook read+write); precise attribution accelerates "
        "revocation at wp-admin > WooCommerce > Settings > Advanced > "
        "REST API."
    )
    # Mask check: raw value must not leak.
    assert secret not in [f.match for f in findings]


