def test_round13_patterns_do_not_collide_with_existing_vendors(
    tmp_path: Path,
) -> None:
    """The three Round-13 issuers (WooCommerce ``ck_``, WooCommerce
    ``cs_``, Mailchimp ``<hex>-us<N>``) MUST NOT cross-trigger any
    existing vendor's pattern:

      * WooCommerce ``ck_`` vs. Stripe ``sk_``/``rk_``/``whsec_``:
        different prefix shape (``ck_`` vs. ``sk_`` etc.).
      * WooCommerce ``cs_`` vs. Shopify ``shpss_``/``shppa_``/etc.:
        Shopify prefixes are all 6+ chars (``shpat_`` etc.); WC
        ``cs_`` is 3 chars + body and the body alphabets differ
        (WC uses ``[a-zA-Z0-9]``; Shopify uses ``[a-fA-F0-9]``).
      * Mailchimp ``<hex>-us<N>`` vs. Mailgun ``key-<hex>``: both
        marketing-email-platform vendors, but the prefix is opposite
        (Mailgun's is at the start of the token; Mailchimp's
        ``-us<N>`` is at the end as a suffix), and the body alphabets
        differ slightly.
    """
    file_path = tmp_path / "mixed_round13.py"
    wc_ck = f"ck_{'a' * 32}"
    wc_cs = f"cs_{'b' * 32}"
    mailchimp = f"{'0' * 32}-us6"
    file_path.write_text(
        f'WC_CK = "{wc_ck}"\n'
        f'WC_CS = "{wc_cs}"\n'
        f'MAILCHIMP = "{mailchimp}"\n',
        encoding="utf-8",
    )

    findings = scan_repository(tmp_path, paths=[file_path])
    reasons = [f.reason for f in findings]

    assert "WooCommerce Consumer Key gefunden" in reasons
    assert "WooCommerce Consumer Secret gefunden" in reasons
    assert "Mailchimp API Key gefunden" in reasons

    # No cross-vendor false positives.
    assert "Stripe Live Secret Key gefunden" not in reasons
    assert "Stripe Test Secret Key gefunden" not in reasons
    assert "Stripe Webhook Signing Secret gefunden" not in reasons
    assert "Shopify Shared Secret gefunden" not in reasons
    assert "Mailgun Private API Key gefunden" not in reasons


def test_round13_secrets_are_masked_in_findings(tmp_path: Path) -> None:
    """All Round-13 secret matches must be masked via ``_mask_secret``
    before reporting, so an accidental commit of the scan output does
    NOT leak the underlying token bytes verbatim to subsequent
    consumers (CI logs, alert pages, PR comments). Mirrors the
    masking contract from every prior round's tests."""
    file_path = tmp_path / "round13_mix.py"
    wc_ck = f"ck_{'a' * 32}"
    wc_cs = f"cs_{'b' * 32}"
    mailchimp = f"{'c' * 32}-us6"
    file_path.write_text(
        f'WC_CK = "{wc_ck}"\n'
        f'WC_CS = "{wc_cs}"\n'
        f'MAILCHIMP = "{mailchimp}"\n',
        encoding="utf-8",
    )

    findings = scan_repository(tmp_path, paths=[file_path])
    matches = [f.match for f in findings]

    # Verbatim tokens must NOT appear in any finding.match span.
    for secret in (wc_ck, wc_cs, mailchimp):
        assert secret not in matches, (
            f"Round-13 secret {secret!r} leaked verbatim into findings; "
            f"_mask_secret must redact every secret match."
        )
