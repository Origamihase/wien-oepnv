    """Real-world Bearer emission shapes (curl debug logs, JSON fixtures,
    YAML config, Python dicts) all canonicalise the case differently
    depending on the emitter; the detector must cover them wholesale."""
    file_path = tmp_path / f"fixture_{case_label}.txt"
