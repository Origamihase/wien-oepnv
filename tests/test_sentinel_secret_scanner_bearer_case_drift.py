# ---------------------------------------------------------------------------
# (5) Compiled-regex invariant: ``_BEARER_RE`` flags include
#     ``re.IGNORECASE`` (programmatic pin against future drift back to
#     the case-sensitive shape).
# ---------------------------------------------------------------------------


def test_bearer_re_flags_include_ignorecase() -> None:
    """The compiled ``_BEARER_RE`` must carry the ``re.IGNORECASE`` flag
    so the auth-scheme literal is matched per the RFC 7235 §2.1
    case-insensitive contract. A future regression that reverts to the
    case-sensitive ``re.compile(r"Bearer\\s+...")`` shape fails this
    invariant immediately."""
    import re as _re

    from src.utils.secret_scanner import _BEARER_RE

    assert _BEARER_RE.flags & _re.IGNORECASE, (
        f"_BEARER_RE flags={_BEARER_RE.flags!r} missing re.IGNORECASE. "
        f"RFC 7235 §2.1 requires case-insensitive matching on the "
        f"Bearer auth-scheme literal. ({SENTINEL_BEARER_CASE_DRIFT})"
    )


